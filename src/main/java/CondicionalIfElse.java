package main.java;

public class CondicionalIfElse {
	
	public static void main(String[] args) {
		
		Integer numero_1 = 6;
		
		if(numero_1 == 6){
			System.out.println("Numero es 6");
		}
		
		Integer numero_2 = 9;
		
		if(numero_2 == 3){
			System.out.println("Numero es 3");
		} else {
			System.out.println("Numero no es 3");
		}
		
	}
	
}

