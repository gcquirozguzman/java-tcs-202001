package main.java;

public class CondicionalIfElse {
	
	public static void main(String[] args) {
		
		boolean condicion_1 = true;
		
		if(condicion_1){
			System.out.println("Condicion verdadera");
		}
		
		boolean condicion_2 = false;
		
		if(condicion_2){
			System.out.println("Condicion verdadera");
		} else {
			System.out.println("Condicion false");
		}
		
	}
	
}

