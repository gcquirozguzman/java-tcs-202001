package main.java;

public class TipoDatosDecimales {

	public static void main(String[] args) {
		
		// Tipo de Datos Decimales
		
		float valorFloat = 3.1234567890F;
		
		double valorDouble = 8.123456789012345;
		
		System.out.println("Valor float :" +valorFloat);
		
		System.out.println("Valor double :" +valorDouble);
		
	}

}
