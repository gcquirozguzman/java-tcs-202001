package main.java;

public class CicloFor {
	
	public static void main(String[] args) {
		
		for (int i = 0; i <= 10; i++) {
			System.out.println("Ejecutando el código");
		}
		
		for (int i = 0; i <= 10; i=i+2) {
			System.out.println("Ejecutando el código");
		}
		
		for (int i = 3; i <= 10; i=i+5) {
			System.out.println("Ejecutando el código");
		}
		
	}
	
}

