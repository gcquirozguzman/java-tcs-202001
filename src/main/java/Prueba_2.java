package main.java;

public class Prueba_2 {

	public static void main(String[] args) {
		
		// Casting Automatico
		
		int valor_1 = 20;
		double valor_2 = valor_1;
		
		System.out.println("Valor final : "+valor_2);
		
		// Casting Manual
		
		double valor_A = 20.59;
		byte valor_B = (byte) valor_A;
		
		System.out.println("Valor final : "+valor_B);
		
	}
	
}
