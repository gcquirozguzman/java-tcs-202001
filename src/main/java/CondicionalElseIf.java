package main.java;

public class CondicionalElseIf {
	
	public static void main(String[] args) {
		
		Integer numero = 10;
		
		if(numero == 2){
			System.out.println("Numero es 2");
		} else if(numero == 3){
			System.out.println("Numero es 3");
		} else if(numero == 8){
			System.out.println("Numero es 8");
		} else if(numero == 12){
			System.out.println("Numero es 12");
		} else {
			System.out.println("Numero no es ninguno de los anteriores valores");
		}
		
	}
	
}

