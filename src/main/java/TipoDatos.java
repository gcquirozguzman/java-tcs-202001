package main.java;

public class TipoDatos {

	public static void main(String[] args) {
		
		// Tipo de Datos Enteros (long int short byte)
		
		byte valorByte = 127;
		
		short valorShort = 32767;
		
		int valorInt = 2147483647;
		
		long valorLong = 9223372036854775807L;
		
		System.out.println("Valor Byte :" +valorByte);
		
		System.out.println("Valor Short :" +valorShort);
		
		System.out.println("Valor Int :" +valorInt);
		
		System.out.println("Valor Long :" +valorLong);
		
		// Se crea variable
		int miPrimerInt;
		
		// Se asigna valor
		miPrimerInt = 2020;
		
		// Varias variables en una linea
		int primeraVariable, segundaVariable, terceraVariable;
		int cuartaVariable = 12, sextaVariable = 99;
		

	}

}
